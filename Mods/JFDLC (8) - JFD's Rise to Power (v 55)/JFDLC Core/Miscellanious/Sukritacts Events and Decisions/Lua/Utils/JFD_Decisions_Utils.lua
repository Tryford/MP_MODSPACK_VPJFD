--Player:GetWritingSystemID
function Player.GetWritingSystemID(player)	
	for row in GameInfo.JFD_WritingSystems() do
		if player:HasPolicy(GameInfoTypes[row.PolicyType]) then
			return row.ID
		end
	end
	return -1
end

--Player:SetWritingSystemID
function Player.SetWritingSystemID(player, writingSystemID)	
	local writingSystem = GameInfo.JFD_WritingSystems[writingSystemID]
	player:GrantPolicy(writingSystem.PolicyType, true)
	-- JFD_SendWorldEvent(player:GetID(), Locale.ConvertTextKey("TXT_KEY_WORLD_EVENT_JFD_WRITING_SYSTEM_ADOPTED", player:GetName(), player:GetCivilizationShortDescription()))
end
